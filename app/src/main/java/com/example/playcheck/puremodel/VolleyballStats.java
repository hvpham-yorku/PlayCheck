package com.example.playcheck.puremodel;

public class VolleyballStats {
    private int killsLeft;
    private int killsRight;
    private int acesLeft;
    private int acesRight;
    private int volleyballBlocksLeft;
    private int volleyballBlocksRight;
    private int digsLeft;
    private int digsRight;

    public VolleyballStats() {}

    public int getKillsLeft() { return killsLeft; }
    public void setKillsLeft(int killsLeft) { this.killsLeft = killsLeft; }

    public int getKillsRight() { return killsRight; }
    public void setKillsRight(int killsRight) { this.killsRight = killsRight; }

    public int getAcesLeft() { return acesLeft; }
    public void setAcesLeft(int acesLeft) { this.acesLeft = acesLeft; }

    public int getAcesRight() { return acesRight; }
    public void setAcesRight(int acesRight) { this.acesRight = acesRight; }

    public int getVolleyballBlocksLeft() { return volleyballBlocksLeft; }
    public void setVolleyballBlocksLeft(int volleyballBlocksLeft) { this.volleyballBlocksLeft = volleyballBlocksLeft; }

    public int getVolleyballBlocksRight() { return volleyballBlocksRight; }
    public void setVolleyballBlocksRight(int volleyballBlocksRight) { this.volleyballBlocksRight = volleyballBlocksRight; }

    public int getDigsLeft() { return digsLeft; }
    public void setDigsLeft(int digsLeft) { this.digsLeft = digsLeft; }

    public int getDigsRight() { return digsRight; }
    public void setDigsRight(int digsRight) { this.digsRight = digsRight; }
}
