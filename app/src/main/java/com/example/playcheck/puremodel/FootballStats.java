package com.example.playcheck.puremodel;

public class FootballStats {
    private int shootingLeft;
    private int shootingRight;
    private int attacksLeft;
    private int attacksRight;
    private int possessionLeft;
    private int possessionRight;
    private int cornersLeft;
    private int cornersRight;
    private int yellowCardsLeft;
    private int yellowCardsRight;
    private int redCardsLeft;
    private int redCardsRight;

    public FootballStats() {}

    public int getShootingLeft() { return shootingLeft; }
    public void setShootingLeft(int shootingLeft) { this.shootingLeft = shootingLeft; }

    public int getShootingRight() { return shootingRight; }
    public void setShootingRight(int shootingRight) { this.shootingRight = shootingRight; }

    public int getAttacksLeft() { return attacksLeft; }
    public void setAttacksLeft(int attacksLeft) { this.attacksLeft = attacksLeft; }

    public int getAttacksRight() { return attacksRight; }
    public void setAttacksRight(int attacksRight) { this.attacksRight = attacksRight; }

    public int getPossessionLeft() { return possessionLeft; }
    public void setPossessionLeft(int possessionLeft) { this.possessionLeft = possessionLeft; }

    public int getPossessionRight() { return possessionRight; }
    public void setPossessionRight(int possessionRight) { this.possessionRight = possessionRight; }

    public int getCornersLeft() { return cornersLeft; }
    public void setCornersLeft(int cornersLeft) { this.cornersLeft = cornersLeft; }

    public int getCornersRight() { return cornersRight; }
    public void setCornersRight(int cornersRight) { this.cornersRight = cornersRight; }

    public int getYellowCardsLeft() { return yellowCardsLeft; }
    public void setYellowCardsLeft(int yellowCardsLeft) { this.yellowCardsLeft = yellowCardsLeft; }

    public int getYellowCardsRight() { return yellowCardsRight; }
    public void setYellowCardsRight(int yellowCardsRight) { this.yellowCardsRight = yellowCardsRight; }

    public int getRedCardsLeft() { return redCardsLeft; }
    public void setRedCardsLeft(int redCardsLeft) { this.redCardsLeft = redCardsLeft; }

    public int getRedCardsRight() { return redCardsRight; }
    public void setRedCardsRight(int redCardsRight) { this.redCardsRight = redCardsRight; }
}
