package com.example.playcheck.puremodel;

public class BasketballStats {
    private int pointsLeft;
    private int pointsRight;
    private int reboundsLeft;
    private int reboundsRight;
    private int assistsLeft;
    private int assistsRight;
    private int stealsLeft;
    private int stealsRight;
    private int blocksLeft;
    private int blocksRight;

    public BasketballStats() {}

    public int getPointsLeft() { return pointsLeft; }
    public void setPointsLeft(int pointsLeft) { this.pointsLeft = pointsLeft; }

    public int getPointsRight() { return pointsRight; }
    public void setPointsRight(int pointsRight) { this.pointsRight = pointsRight; }

    public int getReboundsLeft() { return reboundsLeft; }
    public void setReboundsLeft(int reboundsLeft) { this.reboundsLeft = reboundsLeft; }

    public int getReboundsRight() { return reboundsRight; }
    public void setReboundsRight(int reboundsRight) { this.reboundsRight = reboundsRight; }

    public int getAssistsLeft() { return assistsLeft; }
    public void setAssistsLeft(int assistsLeft) { this.assistsLeft = assistsLeft; }

    public int getAssistsRight() { return assistsRight; }
    public void setAssistsRight(int assistsRight) { this.assistsRight = assistsRight; }

    public int getStealsLeft() { return stealsLeft; }
    public void setStealsLeft(int stealsLeft) { this.stealsLeft = stealsLeft; }

    public int getStealsRight() { return stealsRight; }
    public void setStealsRight(int stealsRight) { this.stealsRight = stealsRight; }

    public int getBlocksLeft() { return blocksLeft; }
    public void setBlocksLeft(int blocksLeft) { this.blocksLeft = blocksLeft; }

    public int getBlocksRight() { return blocksRight; }
    public void setBlocksRight(int blocksRight) { this.blocksRight = blocksRight; }
}
